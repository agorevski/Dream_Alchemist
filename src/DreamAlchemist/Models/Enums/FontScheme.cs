namespace DreamAlchemist.Models.Enums;

/// <summary>
/// Defines available font scheme combinations for the game
/// </summary>
public enum FontScheme
{
    /// <summary>
    /// Cinzel (display) + Outfit (body) + Philosopher (accent) - Elegant, mystical
    /// </summary>
    ClassicMystical,

    /// <summary>
    /// Cormorant Garamond (display) + Outfit (body) + Poiret One (accent) - Light, ethereal
    /// </summary>
    EtherealLight,

    /// <summary>
    /// Audiowide (display) + Inter (body) + Philosopher (accent) - Futuristic, surreal
    /// </summary>
    FuturisticDream
}
